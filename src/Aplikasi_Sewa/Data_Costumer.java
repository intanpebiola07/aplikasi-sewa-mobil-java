     public void getdata(){
    // Code Tampil Data
            DefaultTableModel tbm = new DefaultTableModel();
            //membuat nama kolom pada tabel
            tbm.addColumn("ID Costumer");
            tbm.addColumn("Nama Costumer");
            tbm.addColumn("Alamat");
            tbm.addColumn("No KTP");
            tbm.addColumn("Kontak");
            intan_tabel.setModel(tbm);
            db.koneksiDatabase();
            try {
            String query = "SELECT * FROM data_costumer";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
            tbm.addRow(new Object[]{
                rs.getString("id_costumer"),
                rs.getString("nama_costumer"),
                rs.getString("alamat_costumer"),
                rs.getString("no_ktp"),
                rs.getString("kontak_costumer"),
        });
        intan_tabel.setModel(tbm);
        }
        } catch (SQLException e)
        {
    }
 }
    

    private void intan_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_intan_saveActionPerformed

        // Simpan Data
        db.koneksiDatabase();
        String jekel;
        boolean result = false;
        if(intan_jenkel.isSelected()){
            jekel = "Pria";
        }else{
            jekel = "Wanita";
        }

        // kode Simpan---------------
        String sql = "Insert Into data_costumer Values ('" + intan_tfid.getText() +
        "',  '" + intan_tfnama.getText() +
        "','" + jekel +
        "','" + intan_alamat.getText() +
        "','" + intan_tfkontak.getText() +
        "','" + intan_noktp.getText() +
        "')";
        try {
            Statement st = db.getKoneksi().createStatement();
            st.executeUpdate(sql);
            result=true;
            JOptionPane.showMessageDialog(null, "Proses Simpan Sukses !");

            getdata();
        } catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_intan_saveActionPerformed


    private void intan_btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_intan_btncariActionPerformed
        // TODO add your handling code here:
        // Cari Data
        db.koneksiDatabase();
        try {
            String query = "SELECT * FROM data_mobil where id_mobil = '" + intan_cari.getText() + "'";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                id.setText(rs.getString("id_user"));
                no_plat.setText(rs.getString("no_plat"));
                merek.setText(rs.getString("merek"));
                warna.setText(rs.getString("warna"));
                harga_sewa.setText(rs.getString("harga_sewa"));


            }
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan !");
        }
        intan_cari.setText("");
    }//GEN-LAST:event_intan_btncariActionPerformed


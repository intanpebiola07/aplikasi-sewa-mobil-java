     public void getdata(){
    // Code Tampil Data
            DefaultTableModel tbm = new DefaultTableModel();
            //membuat nama kolom pada tabel
            tbm.addColumn("ID Mobil");
            tbm.addColumn("No Plat");
            tbm.addColumn("Merek");
            tbm.addColumn("Warna");
            tbm.addColumn("Harga Sewa");
            intan_tabel.setModel(tbm);
            db.koneksiDatabase();
            try {
            String query = "SELECT * FROM data_mobil";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
            tbm.addRow(new Object[]{
                rs.getString("id_mobil"),
                rs.getString("no_plat"),
                rs.getString("merek"),
                rs.getString("warna"),
                rs.getString("harga_sewa"),
                
        });
        intan_tabel.setModel(tbm);
        }
        } catch (SQLException e)
        {
    }
 }
    
    
